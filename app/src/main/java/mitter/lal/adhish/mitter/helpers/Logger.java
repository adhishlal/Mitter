package mitter.lal.adhish.mitter.helpers;

import android.util.Log;

/**
 * Created by Adhish on 11/03/18.
 */

public class Logger {

    public static void d(String TAG, String e) {
        Log.d(TAG, e);
    }

    public static void e(String TAG, String e) {
        Log.e(TAG, e);
    }

    public static void i(String TAG, String e) {
        Log.i(TAG, e);
    }

    public static void v(String TAG, String e) {
        Log.v(TAG, e);
    }


}
